    public void DadosPoltrona(int p){
      try{ 
            file = p+".txt";
            
            File F = new File(file+".txt");
            
            if(F.exists()){
            }
            
            BufferedReader br = new BufferedReader(new FileReader("C:/SistemaReserva/Compras/"+file));
            int i =0;
            while(br.ready()){
            dados[i] = br.readLine();
            i++;
        }
        jLabel12.setText(""+dados[0]);
        jLabel13.setText(""+dados[1]);
        jLabel14.setText(""+dados[2]);
        jLabel15.setText(""+dados[3]);
        jLabel16.setText(""+dados[4]);
        
        
        br.close();
        dados = null;
        file = "";
        
        } catch (FileNotFoundException ex) {
            JOptionPane.showMessageDialog(null,"Poltrona Livre");
        } catch (IOException ex) {
            Logger.getLogger(Poltronas.class.getName()).log(Level.SEVERE, null, ex);
        }
       
       
   }
    
    
