        int valor = Integer.parseInt(jTextField1.getText());
       
        c1.Sacar(valor);
       
        if(c1.getA()){
            int Q100 = valor/100;
            int Q50 =(valor - (Q100*100))/50;
            int Q20 = (valor - ((Q100*100)+(Q50*50)))/20;
            int Q10 = (valor - ((Q100*100)+(Q50*50)+(Q20*20)))/10;
            int Q5 =  (valor - ((Q100*100)+(Q50*50)+(Q20*20)+(Q10*10)))/5;

            jLabel9.setText(""+c1.getSaldo());

            jTextField6.setText(""+Q100);
            jTextField5.setText(""+Q50);
            jTextField4.setText(""+Q20);
            jTextField3.setText(""+Q10);
            jTextField2.setText(""+Q5);
        } else{
            jTextField6.setText("");
            jTextField5.setText("");
            jTextField4.setText("");
            jTextField3.setText("");
            jTextField2.setText("");
        }
